"""
SQLAlchemy ORM models — V1 catalog schema.
All tables use UUID primary keys generated by the DB.
"""
from __future__ import annotations

import uuid
from datetime import datetime

from sqlalchemy import (
    Boolean,
    DateTime,
    Float,
    ForeignKey,
    Index,
    Integer,
    String,
    Text,
    UniqueConstraint,
    func,
)
from sqlalchemy.dialects.postgresql import JSONB, UUID
from sqlalchemy.orm import DeclarativeBase, Mapped, mapped_column, relationship


def _uuid() -> str:
    return str(uuid.uuid4())


class Base(DeclarativeBase):
    pass


# ---------------------------------------------------------------------------
# Assets
# ---------------------------------------------------------------------------


class Asset(Base):
    __tablename__ = "asset"

    id: Mapped[str] = mapped_column(UUID(as_uuid=False), primary_key=True, default=_uuid)
    canonical_path: Mapped[str] = mapped_column(Text, nullable=False, unique=True)
    filename: Mapped[str] = mapped_column(Text, nullable=False)
    extension: Mapped[str] = mapped_column(String(20), nullable=False)
    media_type: Mapped[str] = mapped_column(String(10), nullable=False)  # photo | video
    file_size_bytes: Mapped[int | None] = mapped_column(Integer)
    first_seen_at: Mapped[datetime] = mapped_column(DateTime(timezone=True), server_default=func.now())
    last_seen_at: Mapped[datetime] = mapped_column(DateTime(timezone=True), server_default=func.now(), onupdate=func.now())
    is_missing: Mapped[bool] = mapped_column(Boolean, default=False)
    source_root: Mapped[str | None] = mapped_column(Text)
    created_at: Mapped[datetime] = mapped_column(DateTime(timezone=True), server_default=func.now())
    updated_at: Mapped[datetime] = mapped_column(DateTime(timezone=True), server_default=func.now(), onupdate=func.now())

    hashes: Mapped[list[AssetHash]] = relationship(back_populates="asset", cascade="all, delete-orphan")
    media_info: Mapped[AssetMediaInfo | None] = relationship(back_populates="asset", uselist=False, cascade="all, delete-orphan")
    temporal: Mapped[AssetTemporal | None] = relationship(back_populates="asset", uselist=False, cascade="all, delete-orphan")
    location: Mapped[AssetLocation | None] = relationship(back_populates="asset", uselist=False, cascade="all, delete-orphan")
    thumbnails: Mapped[list[AssetThumbnail]] = relationship(back_populates="asset", cascade="all, delete-orphan")
    keyframes: Mapped[list[Keyframe]] = relationship(back_populates="asset", cascade="all, delete-orphan")
    extraction_runs: Mapped[list[ExtractionRun]] = relationship(back_populates="asset", cascade="all, delete-orphan")

    __table_args__ = (
        Index("ix_asset_media_type", "media_type"),
        Index("ix_asset_source_root", "source_root"),
        Index("ix_asset_first_seen", "first_seen_at"),
    )


class AssetHash(Base):
    __tablename__ = "asset_hash"

    id: Mapped[str] = mapped_column(UUID(as_uuid=False), primary_key=True, default=_uuid)
    asset_id: Mapped[str] = mapped_column(UUID(as_uuid=False), ForeignKey("asset.id", ondelete="CASCADE"), nullable=False)
    hash_type: Mapped[str] = mapped_column(String(20), nullable=False)  # sha256 | phash | dhash
    hash_value: Mapped[str] = mapped_column(Text, nullable=False)
    computed_at: Mapped[datetime] = mapped_column(DateTime(timezone=True), server_default=func.now())

    asset: Mapped[Asset] = relationship(back_populates="hashes")

    __table_args__ = (
        UniqueConstraint("asset_id", "hash_type", name="uq_asset_hash_type"),
        Index("ix_asset_hash_value", "hash_type", "hash_value"),
    )


class AssetMediaInfo(Base):
    __tablename__ = "asset_media_info"

    id: Mapped[str] = mapped_column(UUID(as_uuid=False), primary_key=True, default=_uuid)
    asset_id: Mapped[str] = mapped_column(UUID(as_uuid=False), ForeignKey("asset.id", ondelete="CASCADE"), nullable=False, unique=True)

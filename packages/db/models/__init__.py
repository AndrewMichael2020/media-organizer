    # Raw sources (preserved)
    exif_datetime: Mapped[datetime | None] = mapped_column(DateTime(timezone=True))
    gps_datetime: Mapped[datetime | None] = mapped_column(DateTime(timezone=True))
    filesystem_mtime: Mapped[datetime | None] = mapped_column(DateTime(timezone=True))
    video_creation_time: Mapped[datetime | None] = mapped_column(DateTime(timezone=True))
    timezone_source: Mapped[str | None] = mapped_column(String(50))
    has_conflict: Mapped[bool] = mapped_column(Boolean, default=False)

    asset: Mapped[Asset] = relationship(back_populates="temporal")

    __table_args__ = (Index("ix_asset_temporal_best", "best_timestamp"),)


